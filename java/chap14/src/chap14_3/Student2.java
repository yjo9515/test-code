package chap14_3;

public class Student2 {
	private String name;
	private int score;
	private String sex;
	public Student2(String name, int score, String sex) {
		super();
		this.name = name;
		this.score = score;
		this.sex = sex;
	}
	public String getName() {
		return name;
	}
	public int getScore() {
		return score;
	}
	public String getSex() {
		return sex;
	}
	
	
}
