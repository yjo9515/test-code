package chap04;

public class For02 {

	public static void main(String[] args) {
//		구구단
		for(int i=2;i<10;i++) {
			System.out.println(i+"단");
			for(int k=1;k<10;k++) {
			System.out.println(i+"X"+k+"="+(i*k));
			}
			
				
			System.out.println("--------------------------------");
			System.out.println();
		}
		
	}

}


	